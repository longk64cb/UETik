package com.example.uetik.models;

public class Song {
    public String author;
    public String songName;
    public int duration;
    public String path;

    public String getAuthor() {
        return author;
    }

    public int getDuration() {
        return duration;
    }

    public String getPath() {
        return path;
    }

    public String getSongName() {
        return songName;
    }
}
